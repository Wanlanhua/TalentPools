package com.talentpool.po;

public class Educationbackground {
    private Integer id;
    private String ids;
    private String eusername;

    private String estartdate;

    private String eenddate;

    private String schoolname;

    private String degree;

    private String schoollocation;

    private String takedegree;

    
    

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getIds() {
		return ids;
	}

	public void setIds(String ids) {
		this.ids = ids;
	}

	public String getEusername() {
		return eusername;
	}

	public void setEusername(String eusername) {
		this.eusername = eusername;
	}

	public String getEstartdate() {
		return estartdate;
	}

	public void setEstartdate(String estartdate) {
		this.estartdate = estartdate;
	}

	public String getEenddate() {
		return eenddate;
	}

	public void setEenddate(String eenddate) {
		this.eenddate = eenddate;
	}

	public String getSchoolname() {
		return schoolname;
	}

	public void setSchoolname(String schoolname) {
		this.schoolname = schoolname;
	}

	public String getDegree() {
		return degree;
	}

	public void setDegree(String degree) {
		this.degree = degree;
	}

	public String getSchoollocation() {
		return schoollocation;
	}

	public void setSchoollocation(String schoollocation) {
		this.schoollocation = schoollocation;
	}

	public String getTakedegree() {
		return takedegree;
	}

	public void setTakedegree(String takedegree) {
		this.takedegree = takedegree;
	}

    
    
    
}